dhcp_struct_format = '\
        BBBB\
        I\
        HH\
        I\
        I\
        I\
        I\
        s\
        s\
        s\
        '

class DhcpServer(DatagramServer):
    def handle(self, data, address):
        print(f'{address} sent: {data}')
        print(struct.unpack(dhcp_struct_format, data))

if __name__ == '__main__':
    DhcpServer(':10067').serve_forever()
